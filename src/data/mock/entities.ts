function withChemicalContext(entity: Omit<ExplorerEntity, "chemicalMarkers" | "chemicalHighlights">): ExplorerEntity {
  const chemicalMarkers = chemicalMarkersByEntityId[entity.id];
  if (!chemicalMarkers) {
    throw new Error(`Missing chemical markers for fallback entity: ${entity.id}`);
  }

  const chemicalHighlights = chemicalHighlightsByEntityId[entity.id];
  if (!chemicalHighlights) {
    throw new Error(`Missing chemical highlights for fallback entity: ${entity.id}`);
  }

  return {
    ...entity,
    chemicalMarkers,
    chemicalHighlights,
  };
}

