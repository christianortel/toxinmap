CREATE TYPE source_program_tier AS ENUM ('us_v1_core', 'global_v2_planned', 'literature_editorial');

CREATE TABLE geographies (
  id SERIAL PRIMARY KEY,
  slug VARCHAR(140) UNIQUE NOT NULL,
  name TEXT NOT NULL,
  geography_type VARCHAR(64) NOT NULL,
  geographic_level geographic_level NOT NULL,
  summary TEXT,
  notes TEXT,
  state_code VARCHAR(2),
  country_code VARCHAR(2) DEFAULT 'US',
  centroid geometry(Point, 4326),
  boundary geometry(Geometry, 4326),
  source_ids JSONB DEFAULT '[]'::jsonb NOT NULL,
  source_name TEXT,
  source_url TEXT,
  source_updated_at TIMESTAMPTZ,
  ingestion_version VARCHAR(64) DEFAULT 'v1_draft',
  metadata JSONB DEFAULT '{}'::jsonb NOT NULL,
  created_at TIMESTAMPTZ DEFAULT NOW() NOT NULL,
  updated_at TIMESTAMPTZ DEFAULT NOW() NOT NULL
);

