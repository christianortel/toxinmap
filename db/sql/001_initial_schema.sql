CREATE TYPE layer_group AS ENUM ('official', 'emerging', 'wildlife', 'reproductive', 'legal');
CREATE TYPE evidence_type AS ENUM (
  'direct_measurement',
  'proxy',
  'screening_signal',
  'literature_evidence',
  'editorial_case_study'
);
CREATE TYPE confidence_level AS ENUM ('low', 'moderate', 'high');
CREATE TYPE source_type AS ENUM (
  'federal_registry',
  'federal_research',
  'academic_literature',
  'journalism',
  'global_statistical',
  'global_infrastructure',
  'hydrology_framework'
);
CREATE TYPE geographic_level AS ENUM (
  'facility',
  'site',
  'watershed',
  'county',
  'state',
  'regional',
  'national',
  'global',
  'literature_cluster'
);
CREATE TYPE update_cadence AS ENUM (
  'daily',
  'monthly',
  'quarterly',
  'annual',
  'periodic',
  'irregular',
